class FCGA(nn.Module):
    def __init__(self, inp, oup, image_size, downsample=True):
        super(FCGA, self).__init__()
        # the top route
        self.conv1 = nn.Conv2d(inp, inp, kernel_size=1, stride=2)
        self.relu1 = nn.ReLU()
        # the middle route
        self.conv2_1 = nn.Conv2d(inp, int(inp / 2), kernel_size=1, stride=2)
        self.relu2_1 = nn.ReLU()
        self.conv2_2 = nn.Conv2d(int(inp / 2), int(inp / 2), kernel_size=3, padding=1)
        self.relu2_2 = nn.ReLU()
        # the bottom route
        self.conv3_1 = nn.Conv2d(inp, int(inp / 2), kernel_size=1, stride=2)
        self.relu3_1 = nn.ReLU()
        self.conv3_2 = nn.Conv2d(int(inp / 2), int(inp / 2), kernel_size=5, groups=int(inp / 2), padding=2)
        self.relu3_2 = nn.ReLU()

    def forward(self, x):
        # the top
        t = self.conv1(x)
        t = self.relu1(t)
        # the middle
        m = self.conv2_1(x)
        m = self.relu2_1(m)
        m = self.conv2_2(m)
        m = self.relu2_2(m)
        # the bottom
        b = self.conv3_1(x)
        b = self.relu3_1(b)
        b = self.conv3_2(b)
        b = self.relu3_2(b)
        # b c h w
        y = torch.cat([t, m], dim=1)
        z = torch.cat([y, b], dim=1)
        return z


class Transformer(nn.Module):
    def __init__(self, inp, oup, image_size, heads=8, dim_head=32, downsample=False, dropout=0.):
        super().__init__()
        hidden_dim = int(inp * 4)

        self.ih, self.iw = image_size
        self.downsample = downsample

        if self.downsample:
            self.pool1 = nn.MaxPool2d(3, 2, 1)
            self.pool2 = nn.MaxPool2d(3, 2, 1)
            self.proj = nn.Conv2d(inp, oup, 1, 1, 0, bias=False)

        self.attn = Attention(inp, oup, image_size, heads, dim_head, dropout)
        self.ff = FeedForward(oup, hidden_dim, dropout)

        self.attn = nn.Sequential(
            Rearrange('b c ih iw -> b (ih iw) c'),
            PreNorm(inp, self.attn, nn.LayerNorm),
            Rearrange('b (ih iw) c -> b c ih iw', ih=self.ih, iw=self.iw)
        )
        self.ff = nn.Sequential(
            Rearrange('b c ih iw -> b (ih iw) c'),
            PreNorm(oup, self.ff, nn.LayerNorm),
            Rearrange('b (ih iw) c -> b c ih iw', ih=self.ih, iw=self.iw)
        )

    def forward(self, x):
        if self.downsample:
            x = self.proj(self.pool1(x)) + self.attn(self.pool2(x))
        else:
            x = x + self.attn(x)
        x = x + self.ff(x)
        return x


class CoAtNet(nn.Module):
    def __init__(self, image_size, in_channels, num_blocks, channels, num_classes=1000,
                 block_types=['C', 'C', 'T', 'T', 'F']):
        super().__init__()
        ih, iw = image_size
        # F代表引导模块
        block = {'C': MBConv, 'T': Transformer, 'F': FCGA}

        self.s0 = self._make_layer(
            conv_3x3_bn, in_channels, channels[0], num_blocks[0], (ih // 2, iw // 2))
        self.s1 = self._make_layer(
            block[block_types[0]], channels[0], channels[1], num_blocks[1], (ih // 4, iw // 4))
        self.s5 = self._make_layer(
            block[block_types[4]], channels[1], channels[2], num_blocks[5], (ih // 4, iw // 4))

        self.s2 = self._make_layer(
            block[block_types[1]], channels[1], channels[2], num_blocks[2], (ih // 8, iw // 8))
        self.s6 = self._make_layer(
            block[block_types[4]], channels[2], channels[3], num_blocks[5], (ih // 8, iw // 8))

        # self.s3 = self._make_layer(
        #     block[block_types[2]], channels[2], channels[3], num_blocks[3], (ih // 16, iw // 16))
        # self.s4 = self._make_layer(
        #     block[block_types[3]], channels[3], channels[4], num_blocks[4], (ih // 32, iw // 32))
        self.s3 = self._make_layer(
            block[block_types[2]], channels[2], channels[3], num_blocks[2], (ih // 16, iw // 16))
        self.s4 = self._make_layer(
            block[block_types[3]], channels[3], channels[4], num_blocks[4], (ih // 32, iw // 32))

        self.pool = nn.AvgPool2d(ih // 32, 1)
        self.fc = nn.Linear(channels[4], num_classes, bias=False)
        self.dropout = nn.Dropout(0.2)

    def forward(self, x):
        x = self.s0(x)  # torch.Size([1, 64, 106, 106]) 结果的size
        x = self.s1(x)  # torch.Size([1, 96, 53, 53])
        y1 = self.s5(x)
        x = self.s2(x)
        y2 = self.s6(x)
        z1 = y1 + x
        x = self.s3(z1)
        z2 = y2 + x
        x = self.s4(z2)
        x = self.pool(x).view(-1, x.shape[1])
        x = self.dropout(x)
        x = self.fc(x)
        return x

    def _make_layer(self, block, inp, oup, depth, image_size):
        layers = nn.ModuleList([])
        for i in range(depth):
            if i == 0:
                layers.append(block(inp, oup, image_size, downsample=True))
            else:
                layers.append(block(oup, oup, image_size))
        return nn.Sequential(*layers)


